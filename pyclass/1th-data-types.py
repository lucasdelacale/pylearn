#Dynamic typing of Python

#String - simple text
print(  "Hello world!", type("Hello world!") )
print()

#Int - whole numbers
print(1312, type(1312))
print()

#Float - floating-point numbers
print(13.12,    type(13.12))
print()

#Boolean - true or false
print(10 == 10, type(10 == 10))
print( 10 == 1, type(10 == 1))
print()

